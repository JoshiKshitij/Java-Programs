package Strings;

public class StringInbultMethods {

	public static void main(String[] args) {
		String one = "Kshitij@gmail.com";
		String two = "Kshitij123";
		String three = "9626532552";
		
		System.out.println(one.contains("@") && one.contains("."));
		System.out.println(two.contains("@"));
		System.out.println(three.contains("@"));
		
		
		System.out.println(Integer.parseInt(three));
		System.out.println(Integer.parseInt(one));
		System.out.println(Integer.parseInt(two));
		
		System.out.println();
		
		
		
	}
	
}
